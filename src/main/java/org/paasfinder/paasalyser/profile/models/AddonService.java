package org.paasfinder.paasalyser.profile.models;

public class AddonService {

	private String name;
	private String url;
	private String description;
	private String type;

	public String getName() {
		return name;
	}

	public String getUrl() {
		return url;
	}

	public String getDescription() {
		return description;
	}

	public String getType() {
		return type;
	}

}
